package org.teknixco.learning;

public class Variables {
	public static void main(String[] args) {
		int myNumber = 88; // 32 bit value, most common use
		short myShort = 847; // 16 bit value, research max
		long myLong = 9797; // 64 bit value, really big numbers
		double myDouble = 7.3243; // extra precision, with decimals, most common use
		float myFloat = 324.3f; // f is needed to declare float, short version of double
		char myChar = 'y'; // can hold Unicode character
		boolean myBoolean = false;
		byte myByte = 127;

		// Research Max values for primitive types

		System.out.println(myNumber);
		System.out.println(myShort);
		System.out.println(myLong);
		System.out.println(myDouble);
		System.out.println(myFloat);
		System.out.println(myChar);
		System.out.println(myBoolean);
		System.out.println(myByte);
	}
}
